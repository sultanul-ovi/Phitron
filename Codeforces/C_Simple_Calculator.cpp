#include <iostream>
using namespace std;

int main() {
    long long X, Y;
    cin >> X >> Y;

    // Summation
    cout << X << " + " << Y << " = " << X + Y << endl;

    // Multiplication
    cout << X << " * " << Y << " = " << X * Y << endl;

    // Subtraction
    cout << X << " - " << Y << " = " << X - Y << endl;

    return 0;
}
