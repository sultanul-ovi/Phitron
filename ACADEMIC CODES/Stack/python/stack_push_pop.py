# PYTHON IMPLEMENTATION
# STACK PUSH AND POP OPERATIONS

# stack initialization
stack = []

print('\nInitial stack: ')
print(stack)

# To push an element into the stack, we can use append() function

stack.append('ovi')
stack.append('oishee')
stack.append('lami')

print('\nStack After Pushing few elements: ')
print(stack)

# To pop an element from the stack, we can use pop() fucntion
print('\nPopped Elements are:')
print(stack.pop())
print(stack.pop())
print(stack.pop())

print('\nStack After Popping few elements: ')
print(stack)

