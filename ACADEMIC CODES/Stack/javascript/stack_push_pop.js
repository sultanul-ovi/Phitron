//  JavaScript IMPLEMENTATION
//  STACK PUSH AND POP OPERATIONS

// stack initialization
let stack = [];

console.log('\nInitial stack: ');
console.log(stack);

// To push an element into the stack, we can use push() function

stack.push('ovi');
stack.push('oishee');
stack.push('lami');

console.log('\nStack After Pushing few elements:  ');
console.log(stack);

// To pop an element from the stack, we can use pop() fucntion
console.log('\nPopped Elements are:');
console.log(stack.pop());
console.log(stack.pop());
console.log(stack.pop());

console.log('\nStack After Popping few elements: ');
console.log(stack);

