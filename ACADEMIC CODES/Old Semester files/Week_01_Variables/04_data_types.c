#include <stdio.h>

int main()
{
    int price = 10;
    printf("Price: %d\n", price);

    int price2 = 10.55;
    printf("Price: %d\n", price2);

    float temp = 104.2;
    printf("Temp: %f\n", temp);

    double temp2 = 104.2;
    printf("Temp: %lf\n", temp2);

    char ch = 'a';
    printf("Character: %c\n",ch);


    return 0;
}