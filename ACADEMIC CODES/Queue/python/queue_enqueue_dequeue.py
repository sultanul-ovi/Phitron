# PYTHON IMPLEMENTATION
# QUEUE ENQUEUE AND DEQUEUE OPERATIONS

# queue initialization
queue = []

print("\nInitial queue: ")
print(queue)

# To push an element into the queue, we can use append() function

queue.append('ovi')
queue.append('oishee')
queue.append('lami')

print("\nQueue After Pushing few elements:  ")
print(queue)

# To pop an element from the queue, we can use pop() fucntion
print("\nPopped Elements are:")
print(queue.pop(0))
print(queue.pop(0))
print(queue.pop(0))

print("\nQueue After Popping few elements: ")
print(queue)

