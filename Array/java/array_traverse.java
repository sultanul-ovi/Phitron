// Java Implementation
// Array Traverse

class array_traverse
{
    public static void main(String args[])
    {
        int i;
        int arr[] = {1,2,3,4,5};
        int n = arr.length;

        System.out.println("Array Elements: ");
        for (i=0; i<n; i++)
            System.out.print(arr[i] + " ");


    }
}